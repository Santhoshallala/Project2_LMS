    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String mid = txtmid.getText();
        String membername = txtmname.getText();
        String bookname = txtbook.getText();
        String ReturnDate = txtreturndate.getText();
        String elpdays = txtelap.getText();
        String fine = txtfine.getText();
        
       

        try {
            // TODO add your handling code here:

            pst = con.prepareStatement("insert into return_book(mid,mname,bname,return_date,elp,Fine)VALUES(?,?,?,?,?,?)");

            pst.setString(1, mid);
            pst.setString(2, membername);
            pst.setString(3,bookname);
            pst.setString(4,ReturnDate);
            pst.setString(5,elpdays);
            pst.setString(6,fine);
            int k = pst.executeUpdate();

            pst = con.prepareStatement("delete from issue_book where memberid = ?");
            pst.setString(1, mid);
            pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(this, "Return Succesful");
                txtmid.setText("");
                txtmname.setText("");
                txtbook.setText("");
                txtreturndate.setText("");
                txtelap.setText("");
                txtfine.setText("");
                txtmid.requestFocus();
                
            }
            else{
                JOptionPane.showMessageDialog(this, "Error");
            }

        } catch (SQLException ex) {
            Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void txtmidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtmidKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            String id = txtmid.getText();
        try {
            pst = con.prepareStatement("select m.name,b.BookName,l.returndate,DATEDIFF(now(),l.returndate) as elap from issue_book l JOIN member m ON l.memberid = l.id JOIN book b ON l.bookid = b.id and l.memberid = ?");
            pst.setString(1, id);
            rs = pst.executeQuery();
            
            if(rs.next() == false){
                JOptionPane.showMessageDialog(this, "Member id not found");
            }
            else{
                String mname = rs.getString("m.name");
                String bname = rs.getString("b.BookName");
                
                txtmname.setText(mname.trim());
                txtreturndate.setText(bname.trim());
                
                String date = rs.getString("l.returndate");
                txtreturndate.setText(date);
                
                String elp = rs.getString("elap");
                
                int elaped = Integer.parseInt(elp);
                
                if(elaped > 0){
                    txtelap.setText(elp);
                    int fine = elaped * 100;
                    txtfine.setText(String.valueOf(fine));
                    
                }
                else{
                    txtelap.setText("0");
                    txtfine.setText("0");
                }
   
                

            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
        }

        }
    }//GEN-LAST:event_txtmidKeyPressed

